public class event {
    String name;
    String date;
    String startTime;
    String endTime;
    String notes;
    public event(String name, String date, String startTime, String endTime, String notes){
        this.name = name;
        this.date = date;
        this.startTime = startTime;
        this.endTime = endTime;
        this.notes = notes;
    }
}
